        delta = rho / rho_c
        tau = T_c / T
        _sum = sum(
            entry['n'] * entry['I'] * (entry['I'] - 1) * delta ** (entry['I'] - 2) * tau ** entry['J'] for entry in
            Region3.table30.values() if entry['I'] is not None)
        other_term = - Region3.table30[1]['n'] / delta ** 2
        return other_term + _sum

